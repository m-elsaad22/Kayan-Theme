function scrape__current_price_api(){
	$transienst_id = 'cur_scrape__api';
	$data = get_transient($transienst_id);
	if( empty( $data ) || isset( $_GET['force'] ) ) {
		$data = file_get_contents('https://api.currencyfreaks.com/latest?apikey=0ee79558f5b3485eaaa779106934872b');
		$data = json_decode($data,1);
		set_transient($transienst_id,$data,3600);
	}	
	return $data;
}

function get_current_price_value($currency__item=''){

	$currency__item = ( ( isset( $_COOKIE['currency_mode_v1'] ) && $currency__item == '' ) ) ? $_COOKIE['currency_mode_v1'] : $currency__item;
	if( $currency__item == '' ) $currency__item = get_option('currency');
	if( $currency__item == '' ) $currency__item = 'USD';
	
	$scrape__current_price_api = scrape__current_price_api();

	return number_format((float) $scrape__current_price_api['rates'][$currency__item], 2, '.', '');
	
}

function get_site_currency( $currency__item='',$currency__shows=array()){
	$currency__item = ( ( isset( $_COOKIE['currency_mode_v1'] ) && $currency__item == '' ) ) ? $_COOKIE['currency_mode_v1'] : $currency__item;
	if( $currency__item == '' ) $currency__item = get_option('currency');
	if( $currency__item == '' ) $currency__item = 'USD';

	if( empty( $currency__shows ) ) $currency__shows = get_option('currency__shows');

	$currency__shows = ( (  is_array( $currency__shows ) ) ) ? $currency__shows : array();
	foreach ( $currency__shows as $k => $v ) {
		if( $v['item__id'] == $currency__item ){
			return $v;
		}
	}
}

function price($price_value,$currency__item=''){

	$currency__item = ( ( isset( $_COOKIE['currency_mode_v1'] ) && $currency__item == '' ) ) ? $_COOKIE['currency_mode_v1'] : $currency__item;
	if( $currency__item == '' ) $currency__item = get_option('currency');
	if( $currency__item == '' ) $currency__item = 'USD';

	$get_current_price_value = get_current_price_value($currency__item);
	return (INT) $price_value*$get_current_price_value;
}